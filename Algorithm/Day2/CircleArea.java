package Day2;

public class CircleArea {

    public static void main(String[] args) {
        final double PI = 3.14;
        double radius = 10.0;
        double circleArea = radius * radius * PI;
        System.out.println("원의 면적 = " + circleArea);

        int height = 7;
        int width = 43;
        System.out.println(height * width);
    }

}
